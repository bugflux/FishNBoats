	Observable observable;

	public void setObservable(Observable observable) {
		observable.addObserver(this);
		this.observable = observable;
	}

	@Override
	public void update(Observable o, Object arg) {
		if (o != observable) {
			System.out.println("Contacted by wrong observable");
			return;
		}

		if (arg instanceof String[]) {
			String[] params = (String[]) arg;
			Graphics graphics = jPanel1.getGraphics();
			int x = 100, y = 20 + 20 * (Integer.valueOf(params[1])), radius = 7;

			if (params[0].compareToIgnoreCase("alive") == 0) {
				graphics.drawOval(x - radius, y - radius, 2 * radius, 2 * radius);
				graphics.setColor(Color.WHITE);
				graphics.fillOval(x - radius, y - radius, 2 * radius, 2 * radius);
				try {
					Thread.sleep(150);
				} catch (InterruptedException ex) {
					Logger.getLogger(Faina.class.getName()).log(Level.SEVERE, null, ex);
				}
				graphics.drawOval(x - radius, y - radius, 2 * radius, 2 * radius);
				graphics.setColor(Color.GREEN);
				graphics.fillOval(x - radius, y - radius, 2 * radius, 2 * radius);

			} else {
				graphics.drawOval(x - radius, y - radius, 2 * radius, 2 * radius);
				graphics.setColor(Color.RED);
				graphics.fillOval(x - radius, y - radius, 2 * radius, 2 * radius);
			}
		}
	}
